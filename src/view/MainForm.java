    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:

        //
        if (checkData()) {
            String body = txtBoxBody.getText();
            String subject = txtBoxSubject.getText();
            String toEmail = txtBoxToEmail.getText();
            MailData _mail = new MailData(toEmail, subject, body);
            if (SendMail.send(userEmail, userPass, _mail)) {
                JOptionPane.showMessageDialog(rootPane, "Gửi thành công !!!");
                lbStatus.setText("Thành công");
                lbStatus.setForeground(Color.GREEN);
            } else {
                lbStatus.setText("Thất bại");
                lbStatus.setForeground(Color.red);
                JOptionPane.showMessageDialog(rootPane,
                        "Gữi không thành công\n Kiểm tra lại thông tin tài khoản !");
            }
        }

    }//GEN-LAST:event_btnSendActionPerformed


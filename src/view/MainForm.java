    private boolean checkData() {
        if (txtBoxBody.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Nội dung không được để trống !");
            return false;
        }
        if (txtBoxToEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Địa chỉ không được để trống !");
            return false;
        }
        if (txtBoxBody.getText().equals("") || userEmail.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Chưa đăng nhập !");
            return false;
        }
        return true;
    }
    
